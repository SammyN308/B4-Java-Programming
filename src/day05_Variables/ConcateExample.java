package day05_Variables;

public class ConcateExample {
    public static void main(String[] args) {

        System.out.println(2 + 3); // this is ADDITION and the result will be 5
        System.out.println(2 + 3 + "Loopcamp"); //Addition and then CONCATENATION which results in > 5Loopcamp
        //                  int + int = int
        //                    int + string ----> String

        System.out.println("Loopcamp" + 2 + 3);   //CONCATENATION + CONCATENATION ---> Loopcamp23
        //                  string + int ---> String -- > Loopcamp2
        //                      string + int ----> string --> Loopcamp23



    }

}


